import { useState } from "react";

export default function Professors() {
  const [query, setQuery] = useState("");

  const filtered = professors.filter((p) => {
    const lower = query.toLowerCase();
    return (
      p.name.toLowerCase().includes(lower) ||
      p.title.toLowerCase().includes(lower) ||
      p.email.toLowerCase().includes(lower)
    );
  });

  return (
    <main className="p-6 text-black">
      <h1 className="text-3xl font-semibold mb-6">UIC CS Faculty</h1>
      {/* Search classes bar */}
      <input
        type="text"
        placeholder="Search professors..."
        className="w-full p-3 mb-6 border rounded-xl shadow-sm focus:outline-none focus:ring-2 focus:ring-blue-500"
        value={query}
        onChange={(e) => setQuery(e.target.value)}
      />

      {/* Grid */}
      <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-6">
        {filtered.map((p) => (
          <div
            key={p.email}
            className="bg-white p-4 rounded-xl shadow hover:shadow-md transition"
          >
            <a
              href={p.link}
              target="_blank"
              className="text-blue-600 hover:underline font-bold"
            >
              {p.name}
            </a>

            <p className="text-sm text-gray-600">{p.title}</p>
            <p className="text-sm text-gray-600 mt-2">{p.email}</p>
          </div>
        ))}
      </div>
    </main>
  );
}
